//C variables
#include<stdio.h>
void main(){
	//declaring a variable
	int a;
	//Initialising a variable
	a=10;
	float k=10.98;
	char initial='p';
	//format specifier
	printf("%d\n",a);
	printf("%f\n",k);
	printf("%c\n",initial);
	
	//Change values
	a=100;
	printf("%d\n",a);
	
	//Multiple Variables
	
	int x=10,y=20,z=30;
	printf("\n%d %d %d",x,y,z);
}
